Convert Temperature
Declare a variable "temperature" and initialise it with a value of 25.5 (in Celsius) and Print it in Celsius and Kelvin(add 273 to temperature in Celsius).
[Note: print the output exactly in the same format as given below.]
Celsius - 25.5
Kelvin - 298.5

temperature <- 25.5
cat( "Celsius -" ,temperature );
cat("\n");
cat( "Kelvin -" , (temperature + 273) ); 